#include "philo.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	if (!s2 || !s1)
		return (1);
	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i])
	{
		i++;
	}
	return (s1[i] - s2[i]);
}
